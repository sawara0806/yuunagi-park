function gradeColor(c, g) {
  const r = Math.round((c[0]*g.mul[0])*(1-g.amt) + g.tint[0]*g.amt);
  const gg = Math.round((c[1]*g.mul[1])*(1-g.amt) + g.tint[1]*g.amt);
  const b = Math.round((c[2]*g.mul[2])*(1-g.amt) + g.tint[2]*g.amt);
  return [Math.min(255,r), Math.min(255,gg), Math.min(255,b)];
}
function gradedInk(mode) {
  const g = PAL[mode].grade, out = {};
  for (const k in INK) out[k] = gradeColor(INK[k], g);
  return out;
}
